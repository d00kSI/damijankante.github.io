// The main button/link component for a menu item.
const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    // Allows the component to wrap a child, inheriting its props.
    asChild?: boolean
    // Indicates if the menu item is currently active.
    isActive?: boolean
    // Content for a tooltip, shown only when the sidebar is collapsed.
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant = "default",
      size = "default",
      tooltip,
      className,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state } = useSidebar()

    // The core button element.
    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-size={size}
        data-active={isActive}
        className={cn(sidebarMenuButtonVariants({ variant, size }), className)}
        {...props}
      />
    )

    // If no tooltip is provided, return the button directly.
    if (!tooltip) {
      return button
    }

    // If tooltip is a string, convert it to a TooltipContent props object.
    if (typeof tooltip === "string") {
      tooltip = {
        children: tooltip,
      }
    }

    // Wrap the button in a Tooltip component.
    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          // The tooltip is only visible on desktop when the sidebar is collapsed.
          hidden={state !== "collapsed" || isMobile}
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

