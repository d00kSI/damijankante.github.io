// Defines the cookie name for storing the sidebar's open/collapsed state.
const SIDEBAR_COOKIE_NAME = "sidebar:state"
// Sets the maximum age for the sidebar state cookie (7 days).
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
// Defines the default width of the expanded sidebar.
const SIDEBAR_WIDTH = "16rem"
// Defines the width of the sidebar on mobile devices.
const SIDEBAR_WIDTH_MOBILE = "18rem"
// Defines the width of the sidebar when it is collapsed to show only icons.
const SIDEBAR_WIDTH_ICON = "3rem"
// Defines the keyboard shortcut (Cmd/Ctrl + B) to toggle the sidebar.
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

// Defines the shape of the context that will be shared among sidebar components.
type SidebarContext = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

// Creates a React context for the sidebar state.
const SidebarContext = React.createContext<SidebarContext | null>(null)

// Custom hook to easily access the sidebar context.
function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

// The main provider component that manages the state of the sidebar.
const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    // Sets the default open state of the sidebar on initial render.
    defaultOpen?: boolean
    // Allows controlling the open state from outside the component.
    open?: boolean
    // Callback function triggered when the open state changes.
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    // Hook to determine if the current view is mobile.
    const isMobile = useIsMobile()
    // State to manage the open/closed state of the sidebar on mobile.
    const [openMobile, setOpenMobile] = React.useState(false)

    // Internal state for the sidebar's open/closed status on desktop.
    // This allows for both controlled (via props) and uncontrolled behavior.
    const [_open, _setOpen] = React.useState(defaultOpen)
    // The effective open state, prioritizing the external prop if provided.
    const open = openProp ?? _open
    // A memoized function to update the open state.
    const setOpen = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const openState = typeof value === "function" ? value(open) : value
        // If an external handler is provided, use it. Otherwise, update internal state.
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }

        // Persists the sidebar state in a cookie.
        document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
      },
      [setOpenProp, open]
    )

    // Helper function to toggle the sidebar based on the device type.
    const toggleSidebar = React.useCallback(() => {
      return isMobile
        ? setOpenMobile((open) => !open)
        : setOpen((open) => !open)
    }, [isMobile, setOpen, setOpenMobile])

    // Effect to add a global keyboard shortcut for toggling the sidebar.
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    // A string state ("expanded" or "collapsed") for easier styling with data attributes.
    const state = open ? "expanded" : "collapsed"

    // Memoized context value to prevent unnecessary re-renders of consumers.
    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH,
                "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
                ...style,
              } as React.CSSProperties
            }
            // The main wrapper for the entire layout (sidebar + content).
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full has-[[data-variant=inset]]:bg-sidebar",
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

// The primary Sidebar component that renders differently on mobile and desktop.
const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    // Determines which side of the screen the sidebar appears on.
    side?: "left" | "right"
    // The visual style of the sidebar.
    variant?: "sidebar" | "floating" | "inset"
    // The behavior when the sidebar is collapsed.
    collapsible?: "offcanvas" | "icon" | "none"
  }
>(
  (
    {
      side = "left",
      variant = "sidebar",
      collapsible = "offcanvas",
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    // If collapsible is 'none', render a simple, static sidebar.
    if (collapsible === "none") {
      return (
        <div
          className={cn(
            "flex h-full w-[--sidebar-width] flex-col bg-sidebar text-sidebar-foreground",
            className
          )}
          ref={ref}
          {...props}
        >
          {children}
        </div>
      )
    }

    // On mobile, render the sidebar as a Sheet (drawer).
    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
              } as React.CSSProperties
            }
            side={side}
          >
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      )
    }

    // On desktop, render the collapsible sidebar.
    return (
      <div
        ref={ref}
        className="group peer hidden md:block text-sidebar-foreground"
        data-state={state}
        data-collapsible={state === "collapsed" ? collapsible : ""}
        data-variant={variant}
        data-side={side}
      >
        {/* This div acts as a spacer, creating a gap for the main content to occupy. */}
        <div
          className={cn(
            "duration-200 relative h-svh w-[--sidebar-width] bg-transparent transition-[width] ease-linear",
            "group-data-[collapsible=offcanvas]:w-0",
            "group-data-[side=right]:rotate-180",
            variant === "floating" || variant === "inset"
              ? "group-data-[collapsible=icon]:w-[calc(var(--sidebar-width-icon)_+_theme(spacing.4))]"
              : "group-data-[collapsible=icon]:w-[--sidebar-width-icon]"
          )}
        />
        {/* This div is the actual, fixed-position sidebar container. */}
        <div
          className={cn(
            "duration-200 fixed inset-y-0 z-10 hidden h-svh w-[--sidebar-width] transition-[left,right,width] ease-linear md:flex",
            side === "left"
              ? "left-0 group-data-[collapsible=offcanvas]:left-[calc(var(--sidebar-width)*-1)]"
              : "right-0 group-data-[collapsible=offcanvas]:right-[calc(var(--sidebar-width)*-1)]",
            // Adjust padding and width for different variants and states.
            variant === "floating" || variant === "inset"
              ? "p-2 group-data-[collapsible=icon]:w-[calc(var(--sidebar-width-icon)_+_theme(spacing.4)_+2px)]"
              : "group-data-[collapsible=icon]:w-[--sidebar-width-icon] group-data-[side=left]:border-r group-data-[side=right]:border-l",
            className
          )}
          {...props}
        >
          <div
            data-sidebar="sidebar"
            // Handles styling for the 'floating' variant.
            className="flex h-full w-full flex-col bg-sidebar group-data-[variant=floating]:rounded-lg group-data-[variant=floating]:border group-data-[variant=floating]:border-sidebar-border group-data-[variant=floating]:shadow"
          >
            {children}
          </div>
        </div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

